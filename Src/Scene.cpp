/**
* @file Scene.cpp
*/
#include "Scene.h"
#include <iostream>

/**
* コンストラクタ.
*
* @param name scene名.
*/
Scene::Scene(const char* name) : name(name)
{
	std::cout << "Scene コンストラクタ: " << name << "\n";
}

/**
* デストラクタ.
*/
Scene::~Scene()
{
	Finalize();
	std::cout << "Scene デストラクタ: " << name << "\n";
}

/**
* sceneを活動状態にする
*/
void Scene::Play()
{
	isActive = true;
	std::cout << "Scene Play: " << name << "\n";
}

/**
* sceneを停止状態にする.
*/
void Scene::Stop()
{
	isActive = false;
	std::cout << "Scene Stop:" << name << "\n";
}

/**
* sceneを表示する.
*/
void Scene::Show()
{
	isVisible = true;
	std::cout << "Scene Show: " << name << "\n";
}

/**
* sceneを非表示にする.
*/
void Scene::Hide()
{
	isVisible = false;
	std::cout << "Scene Hide:" << name << "\n";
}

/**
* scene名を取得する.
*
* @return scene名.
*/
const std::string& Scene::Name() const
{
	return name;
}

/**
* シーンの活動状態を調べる.
*
*@retval true	活動している.
*@retval false  停止している.
*/
bool Scene::IsActive() const
{
	return isActive;
}

/**
* シーンの表示状態を調べる.
*
* @retval treu   表示状態.
* @retval false  非表示状態.
*/
bool Scene::IsVisible() const
{
	return isVisible;
}


